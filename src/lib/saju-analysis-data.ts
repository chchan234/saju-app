/**
 * 스토리텔링 도입부 생성
 */
export function generateStoryIntro(
  ilju: string,
  dominantOheng: string,
  iljuSymbol: IljuSymbol | undefined
): StoryIntro {
  const season = getCurrentSeason();
  const greetings = SEASON_GREETINGS[season];
  const seasonGreeting = greetings[Math.floor(Math.random() * greetings.length)];

  const symbol = iljuSymbol || ILJU_SYMBOLS[ilju];

  let characterSummary = "";
  let lifeTheme = "";

  if (symbol) {
    characterSummary = `당신은 「${symbol.nickname}」의 기운을 타고났습니다. ${symbol.essence}`;
    lifeTheme = symbol.lifeTheme;
  } else {
    // 기본 문구
    characterSummary = `당신의 사주에는 ${dominantOheng}의 기운이 강하게 흐르고 있습니다.`;
    lifeTheme = "자신만의 길을 걸으며 진정한 성공을 이루는 삶";
  }

  const closingRemark = "이제 당신의 사주를 하나하나 풀어보겠습니다.";

  return {
    seasonGreeting,
    characterSummary,
    lifeTheme,
    closingRemark
  };
}

/**
 * 가족/커플용 스토리텔링 도입부 생성
 */
export function generateGroupStoryIntro(
  memberCount: number,
  averageScore: number,
  isFamily: boolean
): string {
  const season = getCurrentSeason();
  const greetings = SEASON_GREETINGS[season];
  const seasonGreeting = greetings[Math.floor(Math.random() * greetings.length)];

  const groupType = isFamily ? "가족" : "두 분";

  let harmonyDescription = "";
  if (averageScore >= 85) {
    harmonyDescription = `${groupType}의 사주는 아름다운 조화를 이루고 있습니다. 서로를 이해하고 지지하는 깊은 인연이 느껴집니다.`;
  } else if (averageScore >= 75) {
    harmonyDescription = `${groupType}의 사주는 서로를 보완하는 좋은 구조입니다. 약간의 노력으로 더욱 깊은 유대를 만들 수 있습니다.`;
  } else if (averageScore >= 65) {
    harmonyDescription = `${groupType}의 사주에는 강점과 성장 기회가 함께 있습니다. 차이를 이해하면 더 강한 유대가 됩니다.`;
  } else if (averageScore >= 55) {
    harmonyDescription = `${groupType}의 사주는 서로 다른 색깔을 가지고 있습니다. 다름을 인정할 때 진정한 조화가 시작됩니다.`;
  } else {
    harmonyDescription = `${groupType}의 사주는 각자 독특한 개성이 강합니다. 다양성이 오히려 풍요로움이 될 수 있습니다.`;
  }

  return `${seasonGreeting}\n\n${harmonyDescription}`;
}
