/**
 * 천간과 지지에서 십이운성 계산
 */
function getTwelveFortune(cheongan: string, jiji: string): TwelveFortuneKey {
  const startJiji = CHEONGAN_START_JIJI[cheongan];
  const yinyang = CHEONGAN_YINYANG[cheongan];

  const startIndex = JIJI_ORDER.indexOf(startJiji);
  const targetIndex = JIJI_ORDER.indexOf(jiji);

  let diff: number;
  if (yinyang === "양") {
    // 양간: 순행 (시계 방향)
    diff = (targetIndex - startIndex + 12) % 12;
  } else {
    // 음간: 역행 (반시계 방향)
    diff = (startIndex - targetIndex + 12) % 12;
  }

  return TWELVE_FORTUNE_ORDER[diff];
}

/**
 * 기둥별 십이운성 분석
 */
function analyzePillarFortune(
  pillar: Pillar,
  ilgan: string,
  pillarType: "year" | "month" | "day" | "hour"
): TwelveFortuneAnalysis | null {
  if (!pillar.jiji) return null;

  const fortune = getTwelveFortune(ilgan, pillar.jiji);
  const info = TWELVE_FORTUNE_INFO[fortune];

  // 기둥별 특화 해석 가져오기
  const pillarSpecificMeaning = PILLAR_FORTUNE_MEANINGS[fortune][pillarType];

  // 기둥별 의미 조정 - 기둥별 특화 해석 사용
  let lifeStageInterpretation = "";
  const positionName = pillarType === "year" ? "년주(年柱)" :
                       pillarType === "month" ? "월주(月柱)" :
                       pillarType === "day" ? "일주(日柱)" : "시주(時柱)";
  const ageRange = pillarType === "year" ? "0-15세" :
                   pillarType === "month" ? "15-30세" :
                   pillarType === "day" ? "30-45세" : "45세 이후";
  const subject = pillarType === "year" ? "조상" :
                  pillarType === "month" ? "부모와 사회활동" :
                  pillarType === "day" ? "본인" : "자녀와 말년";

  lifeStageInterpretation = `${positionName}의 ${fortune}은(는) ${subject}의 운(${ageRange})을 나타냅니다. ${pillarSpecificMeaning}`;

  return {
    name: fortune,
    hanja: TWELVE_FORTUNE_HANJA[fortune],
    position: pillarType === "year" ? "년주" : pillarType === "month" ? "월주" : pillarType === "day" ? "일주" : "시주",
    meaning: pillarSpecificMeaning,  // 기둥별 특화 의미 사용
    lifeStageInterpretation,
    advice: info.advice,
  };
}

/**
 * 인생 흐름 그래프 데이터 생성
 */
function generateLifeFlowGraph(
  fortunes: (TwelveFortuneAnalysis | null)[]
): { ages: number[]; scores: number[]; peaks: number[]; valleys: number[] } {
  const ages = [7, 22, 37, 55];
  const scores: number[] = [];
  const peaks: number[] = [];
  const valleys: number[] = [];

  fortunes.forEach((fortune, index) => {
    if (fortune) {
      const energy = FORTUNE_ENERGY[fortune.name as TwelveFortuneKey];
      scores.push(energy);
      if (energy >= 80) peaks.push(ages[index]);
      if (energy <= 30) valleys.push(ages[index]);
    } else {
      scores.push(50); // 기본값
    }
  });

  return { ages, scores, peaks, valleys };
}

/**
 * 종합 운성 분석
 */
function analyzeOverallFortune(
  fortunes: (TwelveFortuneAnalysis | null)[]
): string {
  const validFortunes = fortunes.filter(Boolean) as TwelveFortuneAnalysis[];
  const fortuneNames = validFortunes.map(f => f.name as TwelveFortuneKey);

  // 좋은 운성 수
  const goodFortunes: TwelveFortuneKey[] = ["장생", "관대", "건록", "제왕"];
  const challengingFortunes: TwelveFortuneKey[] = ["사", "묘", "절"];

  const goodCount = fortuneNames.filter(f => goodFortunes.includes(f)).length;
  const challengingCount = fortuneNames.filter(f => challengingFortunes.includes(f)).length;

  let summary = "";

  if (goodCount >= 2) {
    summary = "십이운성 중 좋은 운성이 많아 전반적으로 활력 있는 인생입니다. 목표를 높게 잡고 적극적으로 도전하세요.";
  } else if (challengingCount >= 2) {
    summary = "도전적인 운성이 있어 인생에서 역경을 겪을 수 있지만, 이를 통해 내면적으로 크게 성장합니다. 정신적 수양에 힘쓰세요.";
  } else {
    summary = "다양한 운성이 섞여 있어 인생의 다양한 면을 경험합니다. 각 시기에 맞게 유연하게 대처하세요.";
  }

  // 특별한 조합 체크
  if (fortuneNames.includes("제왕")) {
    summary += " 제왕운성이 있어 리더십과 카리스마가 강합니다.";
  }
  if (fortuneNames.includes("장생") && fortuneNames.includes("건록")) {
    summary += " 장생과 건록이 함께 있어 꾸준한 성장과 안정된 수입이 기대됩니다.";
  }
  if (fortuneNames.includes("목욕")) {
    summary += " 목욕운성이 있어 매력적인 기운이 있으며 이성에게 인기가 많습니다.";
  }

  return summary;
}

/**
 * 인생 핵심 단계 생성
 */
function generateKeyLifeStages(
  fortunes: (TwelveFortuneAnalysis | null)[]
): Chapter6Result["keyLifeStages"] {
  const stages = [
    { stage: "조년기", age: "0-15세" },
    { stage: "청년기", age: "15-30세" },
    { stage: "중년기", age: "30-45세" },
    { stage: "말년기", age: "45세 이후" },
  ];

  return stages.map((s, index) => {
    const fortune = fortunes[index];
    if (fortune) {
      return {
        stage: s.stage,
        age: s.age,
        fortune: fortune.name,
        advice: fortune.advice,
      };
    }
    return {
      stage: s.stage,
      age: s.age,
      fortune: "정보 없음",
      advice: "시주 정보가 필요합니다.",
    };
  });
}

