/**
 * 공망 해석 생성
 */
function analyzeGongmang(
  dayGanji: string,
  branches: string[]
): Chapter1Result["gongmang"] {
  const gongmangPositions = findGongmang(dayGanji);

  // 사주 내 공망 해당 지지 확인
  const affectedBranches = branches.filter(b => gongmangPositions.includes(b));

  let meaning = "";
  let lifeImpact = "";
  let remedy = "";

  if (gongmangPositions.length === 0) {
    meaning = "공망 정보를 찾을 수 없습니다.";
    lifeImpact = "";
    remedy = "";
  } else if (affectedBranches.length === 0) {
    meaning = `공망 위치는 ${gongmangPositions.map(g => g + "(" + JIJI_HANJA[g] + ")").join(", ")}이나, 사주 내에 해당 지지가 없어 공망의 영향이 적습니다.`;
    lifeImpact = "공망의 직접적인 영향은 적으나, 대운이나 세운에서 해당 지지가 올 때 주의가 필요합니다.";
    remedy = "공망 지지에 해당하는 방위나 물건을 가까이하면 공망의 기운을 채울 수 있습니다.";
  } else {
    const positionNames = affectedBranches.map(b => {
      const idx = branches.indexOf(b);
      const pillarNames = ["년지", "월지", "일지", "시지"];
      return pillarNames[idx] || b;
    });

    meaning = `공망 위치 ${gongmangPositions.map(g => g + "(" + JIJI_HANJA[g] + ")").join(", ")} 중 ${positionNames.join(", ")}에 공망이 있습니다. 공망(空亡)은 '비어있는 기운'으로, 해당 자리의 기운이 약해지거나 불안정할 수 있음을 의미합니다.`;

    if (positionNames.includes("년지")) {
      lifeImpact += "년지 공망: 조상운이나 사회적 배경에서 공허함을 느낄 수 있습니다. ";
    }
    if (positionNames.includes("월지")) {
      lifeImpact += "월지 공망: 부모와의 인연이 얕거나 청년기에 방황할 수 있습니다. ";
    }
    if (positionNames.includes("일지")) {
      lifeImpact += "일지 공망: 배우자운에 변화가 있거나 결혼 생활에서 공허함을 느낄 수 있습니다. ";
    }
    if (positionNames.includes("시지")) {
      lifeImpact += "시지 공망: 자녀운이나 말년 운에서 아쉬움이 있을 수 있습니다. ";
    }

    remedy = "공망의 영향을 줄이려면 해당 지지의 오행을 보충하거나, 정신적으로 채우는 활동(종교, 봉사, 명상 등)이 도움이 됩니다. 또한 공망은 오히려 물질보다 정신적 영역에서 성취를 이루는 힘이 되기도 합니다.";
  }

  return {
    positions: gongmangPositions.map(g => `${g}(${JIJI_HANJA[g]})`),
    meaning,
    lifeImpact: lifeImpact.trim(),
    remedy,
  };
}

/**
 * 천간 관계 분석 (합/충)
 */
function analyzeStemRelations(stems: string[]): Chapter1Result["stemRelations"] {
  const combinations: { stems: string[]; type: string; effect: string }[] = [];
  const clashes: { stems: string[]; type: string; effect: string }[] = [];

  // 모든 천간 쌍 검사
  for (let i = 0; i < stems.length; i++) {
    for (let j = i + 1; j < stems.length; j++) {
      const stem1 = stems[i];
      const stem2 = stems[j];
      if (!stem1 || !stem2) continue;

      // 합 체크
      const hapInfo = CHEONGAN_HAP[stem1];
      if (hapInfo && hapInfo.partner === stem2) {
        const pillarNames = ["년간", "월간", "일간", "시간"];
        combinations.push({
          stems: [`${stem1}(${CHEONGAN_HANJA[stem1]})`, `${stem2}(${CHEONGAN_HANJA[stem2]})`],
          type: hapInfo.name,
          effect: `${pillarNames[i]}과 ${pillarNames[j]}이 합을 이루어 ${hapInfo.result}의 기운이 생성됩니다. 두 기둥 간의 조화와 협력을 의미합니다.`,
        });
      }

      // 충 체크
      const chungInfo = CHEONGAN_CHUNG[stem1];
      if (chungInfo && chungInfo.partner === stem2) {
        const pillarNames = ["년간", "월간", "일간", "시간"];
        clashes.push({
          stems: [`${stem1}(${CHEONGAN_HANJA[stem1]})`, `${stem2}(${CHEONGAN_HANJA[stem2]})`],
          type: chungInfo.name,
          effect: `${pillarNames[i]}과 ${pillarNames[j]}이 충을 이루어 갈등이나 변화의 에너지가 있습니다. 적극적으로 활용하면 추진력이 됩니다.`,
        });
      }
    }
  }

  return { combinations, clashes };
}

/**
 * 지지 관계 분석 (합/충/형/파/해)
 */
function analyzeBranchRelations(branches: string[]): Chapter1Result["branchRelations"] {
  const combinations: { branches: string[]; type: string; effect: string }[] = [];
  const clashes: { branches: string[]; type: string; effect: string }[] = [];
  const punishments: { branches: string[]; type: string; effect: string }[] = [];
  const harms: { branches: string[]; type: string; effect: string }[] = [];
  const destructions: { branches: string[]; type: string; effect: string }[] = [];

  const pillarNames = ["년지", "월지", "일지", "시지"];
  const validBranches = branches.filter(b => b);

  // 삼합 체크
  for (const samhap of JIJI_SAMHAP) {
    const matchedBranches = samhap.branches.filter(b => validBranches.includes(b));
    if (matchedBranches.length >= 2) {
      const positions = matchedBranches.map(b => {
        const idx = branches.indexOf(b);
        return `${pillarNames[idx]}(${b})`;
      });
      combinations.push({
        branches: matchedBranches.map(b => `${b}(${JIJI_HANJA[b]})`),
        type: matchedBranches.length === 3 ? samhap.name : `${samhap.name.replace("三合", "반합")}`,
        effect: matchedBranches.length === 3
          ? `${positions.join(", ")}이 완전한 삼합을 이루어 ${samhap.result}의 강한 기운이 형성됩니다.`
          : `${positions.join(", ")}이 반합을 이루어 ${samhap.result}의 기운이 작용합니다.`,
      });
    }
  }

  // 방합 체크
  for (const banghap of JIJI_BANGHAP) {
    const matchedBranches = banghap.branches.filter(b => validBranches.includes(b));
    if (matchedBranches.length >= 2) {
      const positions = matchedBranches.map(b => {
        const idx = branches.indexOf(b);
        return `${pillarNames[idx]}(${b})`;
      });
      combinations.push({
        branches: matchedBranches.map(b => `${b}(${JIJI_HANJA[b]})`),
        type: banghap.name,
        effect: `${positions.join(", ")}이 방합을 이루어 ${banghap.result}의 기운이 모입니다.`,
      });
    }
  }

  // 육합, 충, 형, 파, 해 체크 (쌍별)
  for (let i = 0; i < branches.length; i++) {
    for (let j = i + 1; j < branches.length; j++) {
      const b1 = branches[i];
      const b2 = branches[j];
      if (!b1 || !b2) continue;

      // 육합
      const yukHapInfo = JIJI_YUKHAP[b1];
      if (yukHapInfo && yukHapInfo.partner === b2) {
        combinations.push({
          branches: [`${b1}(${JIJI_HANJA[b1]})`, `${b2}(${JIJI_HANJA[b2]})`],
          type: yukHapInfo.name,
          effect: `${pillarNames[i]}과 ${pillarNames[j]}이 육합하여 ${yukHapInfo.result}의 기운으로 화합합니다.`,
        });
      }

      // 충
      const chungInfo = JIJI_CHUNG[b1];
      if (chungInfo && chungInfo.partner === b2) {
        clashes.push({
          branches: [`${b1}(${JIJI_HANJA[b1]})`, `${b2}(${JIJI_HANJA[b2]})`],
          type: chungInfo.name,
          effect: `${pillarNames[i]}과 ${pillarNames[j]}이 충하여 변화와 이동의 기운이 있습니다.`,
        });
      }

      // 파
      const paInfo = JIJI_PA[b1];
      if (paInfo && paInfo.partner === b2) {
        destructions.push({
          branches: [`${b1}(${JIJI_HANJA[b1]})`, `${b2}(${JIJI_HANJA[b2]})`],
          type: paInfo.name,
          effect: `${pillarNames[i]}과 ${pillarNames[j]}이 파하여 관계의 깨짐이나 방해의 기운이 있습니다.`,
        });
      }

      // 해
      const haeInfo = JIJI_HAE[b1];
      if (haeInfo && haeInfo.partner === b2) {
        harms.push({
          branches: [`${b1}(${JIJI_HANJA[b1]})`, `${b2}(${JIJI_HANJA[b2]})`],
          type: haeInfo.name,
          effect: `${pillarNames[i]}과 ${pillarNames[j]}이 해하여 서로 해치는 기운이 있습니다.`,
        });
      }
    }
  }

  // 형 체크 (삼형 포함)
  for (const hyung of JIJI_HYUNG) {
    const matchedBranches = hyung.branches.filter(b => validBranches.includes(b));
    // 자형(自刑)은 같은 지지가 2개 이상일 때
    if (hyung.branches.length === 2 && hyung.branches[0] === hyung.branches[1]) {
      const count = validBranches.filter(b => b === hyung.branches[0]).length;
      if (count >= 2) {
        punishments.push({
          branches: [`${hyung.branches[0]}(${JIJI_HANJA[hyung.branches[0]]})`],
          type: hyung.name,
          effect: hyung.effect,
        });
      }
    } else if (matchedBranches.length >= 2) {
      punishments.push({
        branches: matchedBranches.map(b => `${b}(${JIJI_HANJA[b]})`),
        type: hyung.name,
        effect: hyung.effect,
      });
    }
  }

  return { combinations, clashes, punishments, harms, destructions };
}

/**
 * 기둥 상세 분석
 */
function analyzePillar(
  pillar: Pillar,
  pillarType: "year" | "month" | "day" | "hour"
): PillarExpertAnalysis {
  const stem = pillar.cheongan;
  const branch = pillar.jiji;

  const stemInfo = CHEONGAN_MEANINGS[stem] || { nature: "", symbol: "", personality: "" };
  const branchInfo = JIJI_MEANINGS[branch] || { nature: "", season: "", time: "", symbol: "" };
  const stageInfo = PILLAR_LIFE_STAGES[pillarType];

  const stemAnalysis = stem
    ? `${stem}(${CHEONGAN_HANJA[stem]})은 ${stemInfo.nature}로, ${stemInfo.symbol}을 상징합니다. ${stemInfo.personality}`
    : "시간 정보가 없습니다.";

  const branchAnalysis = branch
    ? `${branch}(${JIJI_HANJA[branch]})는 ${branchInfo.nature}로, ${branchInfo.season}을 담당하며, ${branchInfo.symbol}를 나타냅니다.`
    : "시간 정보가 없습니다.";

  const ganji = pillar.ganji || "";
  const combinedMeaning = ganji
    ? `${ganji}(${stem ? CHEONGAN_HANJA[stem] : ""}${branch ? JIJI_HANJA[branch] : ""})는 ${stemInfo.symbol}과 ${branchInfo.symbol}의 결합으로, 천간의 ${pillar.cheonganOheng}과 지지의 ${pillar.jijiOheng} 기운이 함께합니다.`
    : "시간 정보가 없습니다.";

  return {
    ganZhi: {
      stem: (CHEONGAN_HANJA[stem] || stem) as never,
      stemKr: stem as never,
      branch: (JIJI_HANJA[branch] || branch) as never,
      branchKr: branch as never,
      fullHanja: `${CHEONGAN_HANJA[stem] || ""}${JIJI_HANJA[branch] || ""}`,
      fullKr: ganji,
    },
    stemAnalysis,
    branchAnalysis,
    combinedMeaning,
    lifeStage: `${stageInfo.stage} (${stageInfo.age})`,
    symbolism: stageInfo.represents,
  };
}

